function getTranslation(key) {
    const lang = localStorage.getItem('openchat_lang') || 'en';
    if (!translations[lang]) return translations['en'][key] || key;
    return translations[lang][key] || translations['en'][key] || key;
}

function applyTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            if (el.hasAttribute('placeholder')) {
                el.placeholder = getTranslation(key);
            }
            if (el.type === 'button' || el.type === 'submit') {
                el.value = getTranslation(key);
            }
        } else {
            // Keep SVG intact if present, just replace the inner text
            if (el.querySelector('svg')) {
                // Find trailing text node to replace instead of nuking the SVG
                let replaced = false;
                for (let i = 0; i < el.childNodes.length; i++) {
                    const node = el.childNodes[i];
                    if (node.nodeType === Node.TEXT_NODE && node.nodeValue.trim().length > 0) {
                        node.nodeValue = getTranslation(key);
                        replaced = true;
                        break;
                    }
                }
                // if it didn't find a text node, perhaps span inside
                if (!replaced) {
                    const span = el.querySelector('span');
                    if (span) span.textContent = getTranslation(key);
                    else el.append(document.createTextNode(" " + getTranslation(key)));
                }
            } else {
                el.textContent = getTranslation(key);
            }
        }
    });
}
